// SWITCH


let cinta = 2;
let sayang ;

switch(cinta){
    case 1 :
    cinta = "sudah habis sudah";
    sayang = "maaf aku sudah tak sayang"
    break;
    case 2 :
    cinta = "aku tidak suka cinta";
    sayang = "sayangku hilang";
    break;
    case 3:
    cinta = "aku sayang cinta";
    sayang = "cintaku hilang";
    break;
    default :
    cinta = "namu kau tak pernah bhisa merasakan";
    sayang = "ku ikhlas dan bertahan";
    break

}

console.log(cinta, sayang);